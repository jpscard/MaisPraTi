//Aqui está um algoritmo simples em JavaScript para 
//converter uma temperatura em graus Celsius para Fahrenheit.
// O algoritmo lê a temperatura em Celsius, faz o cálculo e então exibe o resultado em Fahrenheit.

// Definindo a temperatura em Celsius
var temperaturaCelsius = 150;

// Calculando a temperatura em Fahrenheit
var temperaturaFahrenheit = (temperaturaCelsius * 9/5) + 32;

// Mostrando o resultado no console
console.log("A temperatura em Fahrenheit é: " + temperaturaFahrenheit);

